
namespace WebPedidos.WSClasses
{
    /// <summary>
    /// Busca Tabelas de Preco do vendedor
    /// </summary>
    public class ClasseVendedorAtributos
    {

        int?    _IdTabela;
        int?    _CodTipPrz;        
        int     _CodVen;
        string  _DesTipPrc;
        string  _DesTipPrz;

        public string DesTipPrc
        {
            get { return _DesTipPrc; }
            set { _DesTipPrc = value; }
        }

        public string DesTipPrz
        {
            get { return _DesTipPrz; }
            set { _DesTipPrz = value; }
        }

        public int CodVen
        {
            get { return _CodVen; }
            set { _CodVen = value; }
        }

        public int? IdTabela
        {
            get { return _IdTabela; }
            set { _IdTabela = value; }
        }

        public int? CodTipPrz
        {
            get { return _CodTipPrz; }
            set { _CodTipPrz = value; }
        }

        public ClasseVendedorAtributos(int CodVen, int? IdTabela, string DesTipPrc, int? CodTipPrz, string DesTipPrz)
        {
            this._IdTabela  = IdTabela;
            this._CodTipPrz = CodTipPrz;
            this._CodVen    = CodVen;
            this._DesTipPrc = DesTipPrc;
            this._DesTipPrz = DesTipPrz;
        }


    }

}